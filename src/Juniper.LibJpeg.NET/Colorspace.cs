namespace BitMiracle.LibJpeg
{
    /// <summary>
    /// Known color spaces.
    /// </summary>
    public enum Colorspace
    {
        /// <summary>
        /// Unspecified colorspace
        /// </summary>
        Unknown,

        /// <summary>
        /// Grayscale
        /// </summary>
        Grayscale,

        /// <summary>
        /// RGB
        /// </summary>
        RGB,

        /// <summary>
        /// YCbCr (also known as YUV)
        /// </summary>
        YCbCr,

        /// <summary>
        /// CMYK
        /// </summary>
        CMYK,

        /// <summary>
        /// YCbCrK
        /// </summary>
        YCCK
    }
}
