namespace System;

/// <summary>
/// The names of the months of a year, for populating drop-down lists.
/// </summary>
public enum Month
{
    None,

    /// <summary>
    /// January is the first month of the year in the Julian and Gregorian calendars and the
    /// first of seven months to have a length of 31 days. The first day of the month is known as
    /// New Year's Day. It is, on average, the coldest month of the year within most of the
    /// Northern Hemisphere (where it is the second month of winter) and the warmest month of the
    /// year within most of the Southern Hemisphere (where it is the second month of summer). In
    /// the Southern hemisphere, January is the seasonal equivalent of July in the Northern
    /// hemisphere and vice versa. /// Ancient Roman observances during this month include
    /// Cervula, and Juvenalia; celebrated January 1, as well as one of three Agonalia,
    /// celebrated January 9, and Carmentalia, celebrated January 11. These dates do not
    /// correspond to the modern Gregorian calendar.
    /// </summary>
    January,

    /// <summary>
    /// February is the second and shortest month of the year in the Julian and Gregorian
    /// calendar with 28 days in common years and 29 days in leap years, with the quadrennial
    /// 29th day being called the leap day. It is the first of five months to have a length of
    /// less than 31 days. /// February is the third month of meteorological winter in the
    /// Northern Hemisphere. In the Southern Hemisphere, February is the last month of summer
    /// (the seasonal equivalent of August in the Northern Hemisphere, in meteorological reckoning).
    /// </summary>
    February,

    /// <summary>
    /// March is the third month of the year in both the Julian and Gregorian calendars. It is
    /// the second of seven months to have a length of 31 days. In the Northern Hemisphere, the
    /// meteorological beginning of spring occurs on the first day of March. The March equinox on
    /// the 20th or 21st marks the astronomical beginning of spring in the Northern Hemisphere
    /// and the beginning of autumn in the Southern Hemisphere, where September is the seasonal
    /// equivalent of the Northern Hemisphere's March.
    /// </summary>
    March,

    /// <summary>
    /// April is the fourth month of the year in the Gregorian calendar, the fifth in the early
    /// Julian, the first of four months to have a length of 30 days, and the second of five
    /// months to have a length of less than 31 days. /// April is commonly associated with the
    /// season of autumn in parts of the Southern Hemisphere, and spring in parts of the Northern
    /// Hemisphere, where it is the seasonal equivalent to October in the Southern Hemisphere and
    /// vice versa.
    /// </summary>
    April,

    /// <summary>
    /// May is the fifth month of the year in the Julian and Gregorian Calendars and the third of
    /// seven months to have a length of 31 days. /// May is a month of spring in the Northern
    /// Hemisphere and autumn in the Southern Hemisphere. Therefore, May in the Southern
    /// Hemisphere is the seasonal equivalent of November in the Northern Hemisphere and vice
    /// versa. Late May typically marks the start of the summer vacation season in the United
    /// States and Canada and ends on Labor Day, first Monday of September.
    /// </summary>
    May,

    /// <summary>
    /// June is the sixth month of the year in the Julian and Gregorian calendars, the second of
    /// four months to have a length of 30 days, and the third of five months to have a length of
    /// less than 31 days. June contains the summer solstice in the Northern Hemisphere, the day
    /// with the most daylight hours, and the winter solstice in the Southern Hemisphere, the day
    /// with the fewest daylight hours (excluding polar regions in both cases). June in the
    /// Northern Hemisphere is the seasonal equivalent to December in the Southern Hemisphere and
    /// vice versa. In the Northern hemisphere, the beginning of the traditional astronomical
    /// summer is 21 June (meteorological summer begins on 1 June). In the Southern hemisphere,
    /// meteorological winter begins on 1 June. /// At the start of June, the sun rises in the
    /// constellation of Taurus; at the end of June, the sun rises in the constellation of
    /// Gemini. However, due to the precession of the equinoxes, June begins with the sun in the
    /// astrological sign of Gemini, and ends with the sun in the astrological sign of Cancer.
    /// </summary>
    June,

    /// <summary>
    /// July is the seventh month of the year (between June and August) in the Julian and
    /// Gregorian Calendars and the fourth of seven months to have a length of 31 days. It was
    /// named by the Roman Senate in honour of Roman general Julius Cæsar, it being the month of
    /// his birth. Prior to that, it was called Quintilis, being the fifth month of the 10-month
    /// calendar. /// It is on average the warmest month in most of the Northern hemisphere,
    /// where it is the second month of summer, and the coldest month in much of the Southern
    /// hemisphere, where it is the second month of winter. The second half of the year commences
    /// in July. In the Southern hemisphere, July is the seasonal equivalent of January in the
    /// Northern hemisphere. /// "Dog days" are considered to begin in early July in the Northern
    /// Hemisphere, when the hot sultry weather of summer usually starts. Spring lambs born in
    /// late winter or early spring are usually sold before 1 July. /// July is the traditional
    /// period known as "fence month," the closed season for deer in England. The end of
    /// England's High Court of Justice Trinity Term takes place on 31 July. July is also the
    /// time in which the elections take place for the Japanese House of Councillors, held every
    /// three years and replacing half of its seats. /// In Ancient Rome, the festival of
    /// Poplifugia was celebrated on 5 July, and Ludi Apollinares was held on 13 July and for
    /// several days afterwards. However, these dates do not correspond to the modern Gregorian calendar.
    /// </summary>
    July,

    /// <summary>
    /// August is the eighth month of the year in the Julian and Gregorian calendars, and the
    /// fifth of seven months to have a length of 31 days.[1] It was originally named Sextilis in
    /// Latin because it was the sixth month in the original ten-month Roman calendar under
    /// Romulus in 753 BC, and March was the first month of the year. About 700 BC, it became the
    /// eighth month when January and February were added to the year before March by King Numa
    /// Pompilius, who also gave it 29 days. Julius Caesar added two days when he created the
    /// Julian calendar in 46 BC (708 AUC), giving it its modern length of 31 days. In 8 BC, it
    /// was renamed in honor of Augustus. According to a Senatus consultum quoted by Macrobius,
    /// he chose this month because it was the time of several of his great triumphs, including
    /// the conquest of Egypt. /// In the Southern Hemisphere, August is the seasonal equivalent
    /// of February in the Northern Hemisphere. In many European countries, August is the holiday
    /// month for most workers. Numerous religious holidays occurred during August in ancient
    /// Rome. /// Certain meteor showers take place in August. The Kappa Cygnids take place in
    /// August, with the dates varying each year. The Alpha Capricornids meteor shower takes
    /// place as early as July 10 and ends at around August 10, and the Southern Delta Aquariids
    /// take place from mid-July to mid-August, with the peak usually around July 28–29. The
    /// Perseids, a major meteor shower, typically takes place between July 17 and August 24,
    /// with the days of the peak varying yearly. The star cluster of Messier 30 is best observed
    /// around August. /// Among the aborigines of the Canary Islands, especially among the
    /// Guanches of Tenerife, the month of August received in the name of Beñesmer or Beñesmen,
    /// which was also the harvest festival held this month.
    /// </summary>
    August,

    /// <summary>
    /// September is the ninth month of the year in the Julian and Gregorian calendars, the third
    /// of four months to have a length of 30 days, and the fourth of five months to have a
    /// length of less than 31 days. In the Northern Hemisphere September is the seasonal
    /// equivalent of March in the Southern Hemisphere. /// In the Northern hemisphere, the
    /// beginning of the meteorological autumn is on 1 September. In the Southern hemisphere, the
    /// beginning of the meteorological spring is on 1 September. /// September marks the
    /// beginning of the ecclesiastical year in the Eastern Orthodox Church. It is the start of
    /// the academic year in many countries, in which children go back to school after the summer
    /// break, sometimes on the first day of the month. /// September (from Latin septem,
    /// "seven") was originally the seventh of ten months on the oldest known Roman calendar,
    /// with March (Latin Martius) the first month of the year until perhaps as late as 153 BC.
    /// After the calendar reform that added January and February to the beginning of the year,
    /// September became the ninth month, but retained its name. It had 29 days until the Julian
    /// reform, which added a day. /// Ancient Roman observances for September include Ludi
    /// Romani, originally celebrated from September 12 to September 14, later extended to
    /// September 5 to September 19. In the 1st century BC, an extra day was added in honor of
    /// the deified Julius Caesar on 4 September. Epulum Jovis was held on September 13. Ludi
    /// Triumphales was held from September 18–22. The Septimontium was celebrated in September,
    /// and on December 11 on later calendars. These dates do not correspond to the modern
    /// Gregorian calendar. In 1752, the British Empire adopted the Gregorian calendar. In the
    /// British Empire that year, September 2 was immediately followed by September 14. ///
    /// September was called "harvest month" in Charlemagne's calendar. September corresponds
    /// partly to the Fructidor and partly to the Vendémiaire of the first French republic. On
    /// Usenet, it is said that September 1993 (Eternal September) never ended. September is
    /// called Herbstmonat, harvest month, in Switzerland. The Anglo-Saxons called the month
    /// Gerstmonath, barley month, that crop being then usually harvested.
    /// </summary>
    September,

    /// <summary>
    /// October is the tenth month of the year in the Julian and Gregorian Calendars and the
    /// sixth of seven months to have a length of 31 days. The eighth month in the old Roman
    /// calendar, October retained its name (from the Latin ôctō meaning "eight") after January
    /// and February were inserted into the calendar that had originally been created by the
    /// Romans. In Ancient Rome, one of three Mundus patet would take place on October 5,
    /// Meditrinalia October 11, Augustalia on October 12, October Horse on October 15, and
    /// Armilustrium on October 19. These dates do not correspond to the modern Gregorian
    /// calendar. Among the Anglo-Saxons, it was known as Winterfylleth, because at this full
    /// moon (fylleth) winter was supposed to begin. /// October is commonly associated with the
    /// season of autumn in the Northern hemisphere and with spring in the Southern hemisphere.
    /// </summary>
    October,

    /// <summary>
    /// November is the eleventh and penultimate month of the year in the Julian and Gregorian
    /// Calendars, the fourth and last of four months to have a length of 30 days, and the fifth
    /// and last of five months to have a length of less than 31 days. November was the ninth
    /// month of the ancient Roman calendar. November retained its name (from the Latin novem
    /// meaning "nine") when January and February were added to the Roman calendar. November is a
    /// month of late spring in the Southern Hemisphere and late autumn in the Northern
    /// Hemisphere. Therefore, November in the Southern Hemisphere is the seasonal equivalent of
    /// May in the Northern Hemisphere and vice versa. In Ancient Rome, Ludi Plebeii was held
    /// from November 4–17, Epulum Jovis was held on November 13, and Brumalia celebrations began
    /// on November 24. These dates do not correspond to the modern Gregorian calendar. ///
    /// November was referred to as Blōtmōnaþ by the Anglo-Saxons. Brumaire and Frimaire were the
    /// months on which November fell in the French Republican Calendar.
    /// </summary>
    November,

    /// <summary>
    /// December is the twelfth and final month of the year in the Julian and Gregorian Calendars
    /// and is the seventh and last of seven months to have a length of 31 days. /// December got
    /// its name from the Latin word decem (meaning ten) because it was originally the tenth
    /// month of the year in the Roman calendar, which began in March. The winter days following
    /// December were not included as part of any month. Later, the months of January and
    /// February were created out of the monthless period and added to the beginning of the
    /// calendar, but December retained its name. /// In Ancient Rome, as one of the four
    /// Agonalia, this day in honor of Sol Indiges was held on December 11, as was Septimontium.
    /// Dies natalis (birthday) was held at the temple of Tellus on December 13, Consualia was
    /// held on December 15, Saturnalia was held December 17–23, Opiconsivia was held on December
    /// 19, Divalia was held on December 21, Larentalia was held on December 23, and the dies
    /// natalis of Sol Invictus was held on December 25. These dates do not correspond to the
    /// modern Gregorian calendar. /// The Anglo-Saxons referred to December–January as
    /// Ġēolamonaþ (modern English: "Yule month"). The French Republican Calendar contained
    /// December within the months of Frimaire and Nivôse.
    /// </summary>
    December
}